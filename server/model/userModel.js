import mongoose  from "mongoose";

const employeSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    mobile: {
        type: String,
        required: true
    },
})

const userModel = mongoose.model("Employe", employeSchema)

export default userModel;